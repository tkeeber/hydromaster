package com.sprint22.apps.android.hydration.dto;

public class WaterAlarm {

	private long nextNotificationTime;
	private long alarmInterval;
	private String alarmTimeString;
	
    WaterAlarm(long nextNotificationTime, long alarmInterval,
			String textAlarmTimeString) {
		super();
		this.nextNotificationTime = nextNotificationTime;
		this.alarmInterval = alarmInterval;
		this.alarmTimeString = textAlarmTimeString;
	}

	public long getNextNotificationTime() {
		return nextNotificationTime;
	}

	public long getAlarmInterval() {
		return alarmInterval;
	}

	public String getAlarmTimeString() {
		return alarmTimeString;
	}

	
}
